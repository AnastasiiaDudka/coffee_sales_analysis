-- 1. Best Market. 
-- Which country generates the highest revenue, and should the company focus more on this market?
SELECT 
	c.Country,
	COUNT(DISTINCT c."Customer ID") as number_of_customers,
	COUNT (o."Order ID") as number_of_orders,
	ROUND(SUM(o.Quantity * p."Unit Price"),2) as total_revenue,
	ROUND(AVG(o.Quantity * p."Unit Price"),2) as average_order_value
FROM orders o 
JOIN customers c 
	ON o."Customer ID" = c."Customer ID" 
JOIN products p 
	ON o."Product ID" = p."Product ID" 
GROUP BY 1
ORDER BY total_revenue desc;
-- The United States is the company's strongest market, generating more revenue than Ireland and the United Kingdom combined. 
-- It also has the largest customer base and the highest number of orders, indicating strong customer demand. 
-- Although the average order value is only slightly higher than in the other markets, the combination of high customer volume and purchase frequency makes the US the company's most valuable market. 
-- If I had a limited marketing budget, I would prioritize the US because it currently delivers the highest return in terms of sales performance.


